/// IO width, precision, fill

#include <iostream>
using namespace std;

int main()
{

    cout<<"abid.com"<<endl;
    cout.width(25);
    cout.fill('.');
    cout<<"abid.com"<<endl;
    cout.width(25);
    cout<<"abid.com"<<endl;
    cout<<"abid.com"<<endl;
    cout.width(20);
    cout.fill(' ');
    cout<<"abid.com"<<endl;
    cout.width(1);
    cout<<"abid.com"<<endl;
}
